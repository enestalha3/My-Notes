sayı = int(input("Lütfen sayıyı giriniz:"))

i = 1 

toplam = 0

while(i < sayı):
    if(sayı % i == 0):
        
        toplam += i
                                
    i += 1 

if(sayı == toplam):
    
    
    print("Sayı mükemmel sayıdır")
    
else:
    print("Sayı mükemmel sayı değildir")
    

                                                                               
    
           





                            

























